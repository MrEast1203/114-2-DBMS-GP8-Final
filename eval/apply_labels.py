def main() -> None:
    src = Path("eval/candidates.jsonl")
    dst = Path("eval/ground-truth.jsonl")

    lines = []
    found = 0
    missing: list[tuple[str, int]] = []
    pos_cnt = 0

    for raw in src.read_text().splitlines():
        if not raw.strip():
            continue
        r = json.loads(raw)
        key = (r["qid"], r["paper_id"])
        if key in LABELS:
            r["label"] = LABELS[key]
            pos_cnt += LABELS[key]
            found += 1
        else:
            r["label"] = None
            missing.append(key)
        lines.append(json.dumps(r, ensure_ascii=False))

    dst.write_text("\n".join(lines) + "\n")

    total = found + len(missing)
    print(f"labelled: {found}/{total}  positive: {pos_cnt}  positive rate: {pos_cnt/found:.2%}")
    if missing:
        print(f"missing labels for {len(missing)} pairs:")
        for q, p in missing[:10]:
            print(f"  ({q}, {p})")
        if len(missing) > 10:
            print(f"  ... +{len(missing)-10} more")

    # Per-query summary
    from collections import Counter
    pos_by_q: Counter = Counter()
    cnt_by_q: Counter = Counter()
    for (q, _), v in LABELS.items():
        cnt_by_q[q] += 1
        if v: pos_by_q[q] += 1
    print()
    print(f"{'qid':<6} {'positive':>9} {'total':>6} {'rate':>6}")
    for q in sorted(cnt_by_q):
        print(f"{q:<6} {pos_by_q[q]:>9} {cnt_by_q[q]:>6} {pos_by_q[q]/cnt_by_q[q]:>6.2%}")


if __name__ == "__main__":
    main()
